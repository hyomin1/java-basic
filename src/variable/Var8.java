package variable;

public class Var8 {
    public static void main(String[] args) {
        //정수
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32768 ~ 32767
        int i = 214748367; // -21억부터 약 21억정도까지
        long l = 9223333333333333L; //많은 숫자 넣을 수 있음 L or l을 뒤에 넣어줌

        //실수
        float f = 10.0f; // 뒤에 f 붙여줌
        double d = 10.0;
    }
}
