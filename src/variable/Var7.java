package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.5; //실수
        boolean c = true; // true, false
        char d = 'A'; //문자 하나, 작은따옴표로 감싸서 사용
        String e = "Hello Java"; //문자열, 문자열을 다루기 위한 특별한 타입, 큰따옴표로 감싸서 사용

        // 100 , 10.5 , true .. 직접 적은 고정된 값을 리터럴이라고함

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
